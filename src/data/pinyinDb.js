import { pinyinToKeys } from './xiaoheMapping'

function single(pinyin, char, level) {
  const keys = pinyinToKeys(pinyin)
  return { content: char, type: 'char', pinyin, keys, chars: [{ char, pinyin, keys }], level }
}

function word(chars) {
  const items = chars.map(([char, pinyin]) => ({ char, pinyin, keys: pinyinToKeys(pinyin) }))
  const keys = items.flatMap(c => c.keys)
  const pinyin = items.map(c => c.pinyin).join(' ')
  const content = items.map(c => c.char).join('')
  return { content, type: 'word', pinyin, keys, chars: items, level: 2 }
}

function idiom(chars) {
  const items = chars.map(([char, pinyin]) => ({ char, pinyin, keys: pinyinToKeys(pinyin) }))
  const keys = items.flatMap(c => c.keys)
  const pinyin = items.map(c => c.pinyin).join(' ')
  const content = items.map(c => c.char).join('')
  return { content, type: 'idiom', pinyin, keys, chars: items, level: 3 }
}

function sentence(chars, level) {
  const items = chars.map(([char, pinyin]) => ({ char, pinyin, keys: pinyinToKeys(pinyin) }))
  const keys = items.flatMap(c => c.keys)
  const pinyin = items.map(c => c.pinyin).join(' ')
  const content = items.map(c => c.char).join('')
  return { content, type: level === 4 ? '长句' : 'short', pinyin, keys, chars: items, level }
}

